# 客户端API包
# 此包包含所有客户端相关的API路由和处理函数
# 提供用户认证、服务发现、账户管理、计费等客户端功能

# 客户端API模块包括：
# - auth.py: 客户端认证相关API
# - discovery.py: 服务发现相关API
# - services.py: 服务调用相关API
# - account.py: 账户管理相关API
# - dashboard.py: 仪表板概览相关API
# - api_keys.py: API密钥管理相关API
# - billing.py: 计费管理相关API
# - logs.py: 使用日志相关API
# - profile.py: 用户资料相关API
# - user_services.py: 用户服务相关API
# - notifications.py: 通知相关API

